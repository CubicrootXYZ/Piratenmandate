<h2>Cookies</h2>
<p>Die Plattform verwendet sogenannte Cookies.<br>
Cookies sind kleine Textdateien, die auf deinem Rechner abgelegt werden und die dein Browser speichert.<br>
Cookies richten auf deinem Rechner keinen Schaden an und enthalten keine Viren.<br>
Die meisten der von uns verwendeten Cookies sind sogenannte „Session-Cookies".<br>
Das heißt, sie werden nach Ende deines Besuchs automatisch gelöscht.<br>
Cookies können es auch ermöglichen, dich nach Verlassen der Website wieder zu erkennen.<br>
Leider wird diese Funktion von einigen Firmen dazu missbraucht, das Surfverhalten von Internetnutzern auszuspionieren.<br>
Die Piraten lehnen ein solches Verhalten als datenschutzwidrig ab.</p>
<p>Wir verwenden Cookies, um deine Einstellungen für deine nächsten Besuche zu verstehen und zu speichern und um aggregierte Daten über unseren Internetverkehr und Interaktionen auf der Seite zu berechnen, damit wir in der Zukunft eine bessere Seitenerfahrung und bessere Werkzeuge anbieten können.</p>
